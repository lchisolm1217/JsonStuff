package com.lighterletter.www.jsonpractice.model;

/**
 * Object model
 */

public class Fellow {
    private String name;
    private String cohort;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCohort() {
        return cohort;
    }

    public void setCohort(String cohort) {
        this.cohort = cohort;
    }
}
